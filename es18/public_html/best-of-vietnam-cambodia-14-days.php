                        <div class="content_li_tour">
                            Tras el desayuno, empezaremos visitar de la ciudad de HoiAn, que representó un puerto de intercambios con extremo oriente hasta finales del s. XIX. Visitaremos la pagoda PhuocKien, el puente cubierto japonés, la casa Tan Ky, con más de 200 años de antigüedad y sus poemas chinos incrustados en madreperla en sus estructuras de madera, un taller de artesanía y recorreremos algunas de las reconocidas 844 casas históricas y templos.
                            El resto del día para pasear por el pintoresco mercado de la ciudad situado junto al río, para compras o para encargarse un vestido o traje a medida en uno de los numerosos sastres o modistas del lugar que realizan pedidos a medida en 24 horas.
                        </div>
                        <div class="title_li_tour">
                            Día 7 Andar en bicicleta - Curso de cocina (D/A)
                        </div>
                        <div class="content_li_tour">
                            Recogida en el hotel y empezarán a ir en bicicleta pasando pos los paisajes rurales hacia el puedo Tra Que. Disfrutarán de un breve descanso con una copa de bienvenida.

                            Visitarán los  jardines de hierbas donde los agricultores locales trabajan en su campo utilizando métodos orgánicos de hace 300 años. Si te interesa, disfrutar de un baño de pies con las hierbas medicinales en el jardín.

                            También podrán probar hacer el papa de arroz. Finalmente aprenderán las formas tradicionales de preparación y cocina de las comidas vietnamitas. Hacer algunas flores y animales de verduras para la decoración. Después de la lección, disfrutar de lo que han cocinado.

                            Retorno al hotel en bicicleta y el resto del día libre. Alojamiento.
                        </div>
                        <div class="title_li_tour">
                            Día 8 Montaña de Mármol - Ciudadela Imperial – En bote en el Río Perfume (D)
                        </div>
                        <div class="content_li_tour">
                            Por la mañana, en coche privado nos dirigiremos hacia Hue. En el camino, haremos una parada en las Montañas de Mármol: son cinco picos de piedra caliza con afloramientos de mármol conocidas por su belleza y su red de cuevas. A los pies de la montaña de mármol, existe un lugar religioso importante desde la época de los Cham, y allí se pueden ver muchos artesanos que trabajan a mano los objetos de mármol finos.

                            También pasaremos por el particular paso de Hai Van cruzando la cordillera para llegar a Hue. Un maravilloso y espectacular recorrido, ya que Hai Van es el paso más alto y ofrece maravillosas oportunidades para sacar buenas fotografías mientras pasamos por poblados de pescadores y campos de arroz de Hue, sobre el océano y la playa de Danang, así como de las vistas panorámicas de la playa de Lang Co, el pueblo de la costa, y la laguna muy por debajo.

                            Por la tarde, visitaremos Kinh Thanh Citadel (Ciudadela Imperial) es el sitio más conocido y emblemático de Hué. Iniciado en 1805 por el reinado del emperador Gia Long, en realidad es un extenso complejo compuesto por la Ciudad Imperial, varios museos, lagos y hermosos jardines, todo dispuesto en una serie de círculos concéntricos. Se terminó en 1832, bajo el reinado del Emperador Ming Mang También visitamos la Ciudad Púrpura Prohibida, construida a principios de 1800 para el uso personal del emperador y atendida por eunucos que no representaban amenaza alguna para las concubinas reales.

                            Finalmente embarcaremos en una barca a lo largo del río Perfume disfrutando el ambiente tranquilo de la ciudad romántica Hue y visitar la pagoda de Thien Mu que fue considerada como el símbolo de Hue. Alojamiento.
                        </div>

                        <div class="title_li_tour">
                            Día 9 Tumba del Emperador – Vuelo a Ho Chi Minh (D)
                        </div>
                        <div class="content_li_tour">
                            Hue es famoso con las siete tumbas de los emperadores Nguyen. Las montañas que rodean Hue albergan estas tumbas. No solo están construidas como lugares para el eterno descanso, sino también como un lugar de esparcimiento y donde se suponen que los emperadores podrán pescar, cazar y disfrutar de sus concubinas durante este eterno descanso. Cada una representa la personalidad única de estos hombres que están enterrados aquí. Hoy visitaremos una de las tumbas, la del emperador KhaiDinh.
                            Por la tarde, tomar el vuelo a Ho Chi Minh. Recepción por un guía de habla español e ir al hotel. Alojamiento en el hotel.
                        </div>
                        <div class="title_li_tour">
                            Día 10 Túneles Cu Chi – Recorrido por la ciudad Ho Chi Minh (D)
                        </div>
                        <div class="content_li_tour">
                            Por la mañana, tendremos una excursión privada a los túneles de Cu Chi, una inmensa red de estrechas galerías subterráneas, cavadas a mano por los guerrilleros para refugiarse y defenderse durante la guerra de Vietnam. La red incluye innumerables puertas trampas, las salas especiales, instalaciones de almacenamiento, fábricas de armas, hospitales de campaña, y cocinas. Hoy en día los túneles se han convertido en una importante atracción turística que dan al visitante una experiencia única y una sensación sobre la vida bajo de la tierra como durante la guerra entre Estados Unidos y Vietnam.

                            Empezaremos las visitas de la tarde por el Museo de las Crímenes de la Guerra. Después visitaremos el Palacio de la Reunificación, que una vez fue el Palacio Presidencial. Fue contra este edificio que los primeros tanques comunistas arremetieron la mañana del 30 de Abril de 1935. Más tarde conoceremos el principal asentamiento colonial de la ciudad de Ho Chi Minh, la Antigua Central de Correos y el ayuntamiento y la Catedral de Notre Dame.
                        </div>
                        <div class="title_li_tour">
                            Día 11 Ho Chi Minh - Cai Be - Delta Mekong –Can Tho (D/A)
                        </div>
                        <div class="content_li_tour">
                            ras el desayuno, nos dirigiremos en coche hasta la zona del Delta Mekong. A la llegada, embarcaremos a un barco para visitar una casa antigua de la zona. Cambiaremos a un bote de remos en los canales pequeños dirigiéndose hacia la isla Tan Phong donde tendremos un breve descanso antes de empezar la ruta en bici para descubrir la vida de los locales. Almorzaremos en un restaurante local. Si el tiempo libre permite podemos tener la oportunidad escuchar la música local “Don ca Tai Tu” hasta el traslado a la ciudad Can Tho. Alojamiento.
                        </div>
                        <div class="title_li_tour">
                            Día 12 Mercado flotante - Casa antigua - Vuelo a SiemReap (D)
                        </div>
                        <div class="content_li_tour">
                            Por la mañanatemprano, saldremos en barco a visitar el famosos Mercado Flotante de CaiRang – el mayor y más colorido mercado en el delta del Mekong. Podremos tomar tantas fotografías como quiera, hablar o negociar con los comerciantes mientras nuestro barco se desliza por entre las caóticas paradas del mercado. Es una imagen impresionante por la enorme variedad de verduras que aquí se venden. Regreso al hotel para desayunar antes de visitar la Casa antigua Binh Thuy construida en el año 1844. Famosa tanto por su estilo arquitectónico y tallas sobre madera, como por su historia, ya que fue el centro neurálgico de las protestas estudiantiles contra los franceses a principios del siglo XX. Regreso a Ho Chi Minhpara tomar el vuelo a SiemReap. Recepción por un guía de habla español e ir al hotel. Alojamiento en el hotel.
                        </div>

                        <div class="title_li_tour">
                            Día 13 Día completo de visitar los templos Angkor (D)
                        </div>
                        <div class="content_li_tour">
                            El día verdaderamente especial está a punto de comenzar: les esperan unos de los más bellos templos de Angkor. Visitarán la Puerta del Sur de AngkorThom (la Grand ciudad), el famoso templo de Bayon construido a finales del siglo XII y el templo de Baphoun dedicado al Dios Hindú Shiva, la Terraza de los Elefantes, la Terraza del Rey  Leproso y Phimeanakas. A continuación, visitarán el Patrimonio de Humanidad de AngkorWat – el edificio religioso más grande del mundo. La última visita de hoy será el templo Ta Prohm, construido en el siglo XII y que permanece casi en su totalidad en su estado original, desde que fue “redescubierto” por exploradores franceses. Rodeado de selva, su laberinto con pasillos de piedra se encuentran cubiertos por las raíces y las ramas de enormes higueras, que se han adherido a la piedra como tentáculos y garras gigantes. Es uno de los mayores templos de Angkor y data de 1186. Los historiadores han destacado su encanto místico.
                        </div>
                        <div class="title_li_tour">
                            Día 14 Día completo de visitar los templos Angkor (D)
                        </div>
                        <div class="content_li_tour">
                            El recorrido continuará con el templo BanteaySrei: el sitio predilecto de casi todo el mundo. El encanto particular de este templo es en su tamaño compacto, muy buen estado de conservación y hermoso tallado decorativo. También visitaremos BanteaySamre: sus muros de arenisca de color rosa están decorados con el tallado y los bajorrelieves más finos en todo Angkor.

                            Por la tarde saldremos a hacer la visita de la aldea flotante de Chong Khneas, localizado a 10 Km al sur de SeamReap.  Visitaremos este pueblo y haremos una excursión en barco de madera por el Lago TonleSap. El “Gran Lago de Cambodia”, uno de los mayores de Asia.

                            Desembarcamos y nos dirigimos a SiemReap pasando por los campos y pueblos. Por último visitaremos un proyecto de desarrollo de la enseñanza de adultos jóvenes, para la talla de piedra, de madera, y las técnicas de aplicaciones de laca, Les Artisansd'Angkor es otro proyecto que trabaja para que el progreso en Camboya puede ir de la mano de la artesanía tradicional.
                        </div>

                        <div class="title_li_tour">
                            Día 15 Final del viaje (D)
