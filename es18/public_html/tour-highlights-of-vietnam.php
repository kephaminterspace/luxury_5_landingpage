                            Por la mañana, procedemos en coche durante 12 km hasta la Cascada de Plata (la más grande de Sapa). Desde aquí podremos disfrutar de una excelente vista a la montaña y el valle verde. Continuaremos en coche al Paso O Quy Ho. En un buen día de tiempo disfrutarás de la espectacular vista del valle y la montaña Fansipan y seguro tendremos tantas fotos maravillosas.

                            Por la tarde pasarás por la aldea MaTra, otro paseo lleno de arrozales. MaTra es una aldea de la minoría étnica de Hmong Negro, una aldea muy pequeñita y luego continuarás el paseo a la aldea Taphin donde encontramos nuestro vehículo para regresar a Sapa.

                            A la hora indicada, traslado a la estación para tomar el tren de regreso a Hanoi. Noche en una cabina privada para los tres.

                            Comentario de la ruta:
                            3 horas de caminata
                        </div>
                        <div class="title_li_tour">
                            Día 6: Hanoi - Bahía de Halong - Una de las 7 Maravillas del mundo (D/A/C)
                        </div>
                        <div class="content_li_tour">
                            Desayuno en el hotel antes de iren shutte bus hacia la impresionante bahía de Ha Long - “donde el dragón desciende al mar”, una de las siete maravillas naturales del mundo. Nos dirigiremos al muelle donde embarcaremos en un barco grande hecho de valiosa madera con estilo tradicional. Disfrutaremos un cóctel de bienvenida, luego degustaremos un delicioso almuerzo con mariscos frescos. El crucero comienza con la visita de una aldea flotante de los pescadores y tendrá oportunidad a ver su vida diaria. Por la tarde seguiremos con la visita de gruta de las sorpresas, una de las más grande de Bahía, luego tiempo libre a practicar natación en el Mar del Sur China (pertenece al Océano Pacifico) o disfruta del sol en la cubierta del barco. Una tranquila noche a bordo de nuestro cómodo junco en cabinas privadas. Alojamiento en el barco.
                        </div>
                        <div class="title_li_tour">
                            Día 7: Bahía de Halong - Hanoi (D/Brunch)
                        </div>
                        <div class="content_li_tour">
                            Después de levantarse y despertarse por el sonido del océano, disfrutaremos el desayuno, y veremos el amanecer en el mar muy romántico. Nos relajaremos mientras el junco continúa nuestra excursión en el paraíso de miles islas rocosas verdes. Después del brunch, volveremos al shuttle bus para regresar a Hanoi. Alojamiento.

                            Note: El itinerario del crucero se puede cambiar depende de cada barco.
                        </div>
                        <div class="title_li_tour">
                            Día 8: Vuelo a Dong Hoi - Parque nacional PhongNhaKeBang (D)
                        </div>
                        <div class="content_li_tour">
                            Traslado al aeropuerto sin guía para tomar el vuelo a Dong Hoi para ir al Parque nacional de PhongNhaKeBang que destacan sobretodo sus cuevas y su paisaje espectacular. Hoy visitaremos dos cuevas principales. Llegada al muelle, embarcaremos a un barco a motor para visitar la cueva de PhongNha, o llamada como "Cueva de Humedad". Durante los 12 kilómetros de recorrido por el ancho río  nos dejarán bonitas imágenes. Gente pescando, niños jugando en la orilla del río y unos cuantos búfalos tomando un agradable baño. 

                            Por la tarde hacemoskayaking en el río Chay y tendremos la oportunidad de admirar las aves, mariposas y libélulas en el camino hacia el punto de encuentro y una oportunidad de refrescarse en una zona de natación encantadora.

                            Luego, nos dirigiremos al PhongNha Lake House para pasar la noche.
                        </div>
                        <div class="title_li_tour">
                            Día 9: Kayking en el río Chay - Ir a Hue (D)
                        </div>
                        <div class="content_li_tour">
                            Tras el desayuno, el coche te recogerá e iremos hacia ThienDuong, "Cueva Paraíso". La Cueva Paraíso fue descubierta por los científicos de la Asociación de Investigación de Espeleología británica en 2005. Esta tiene una longitud de 31 kilómetros y es la cueva seca más larga de Asia. Con las características naturales primitivas, ThienDuong parece una pintura de paisajes, por lo que es muy atractivo para los visitantes.

                            Finalmente, traslado a Hue. Llegada a Hue tarde, a las 18:00 aproximadamente.
                        </div>
                        <div class="title_li_tour">
                            Día 10: Paseo en la aldea Thuy Bieu - Descubrir la ciudadela Imperial (D/A) Guía en español
                        </div>
                        <div class="content_li_tour">
                            Por la mañana, visitaremos KinhThanhCitadel (Ciudadela Imperial) es el sitio más conocido y emblemático de Hué. Iniciado en 1805 por el reinado del emperador Gia Long, en realidad es un extenso complejo compuesto por la Ciudad Imperial, varios museos, lagos y hermosos jardines, todo dispuesto en una serie de círculos concéntricos. Se terminó en 1832, bajo el reinado del Emperador Ming Mang También visitamos la Ciudad Púrpura Prohibida, construida a principios de 1800 para el uso personal del emperador y atendida por eunucos que no representaban amenaza alguna para las concubinas reales.

                            Luego, embarcaremos en una barca a lo largo del río Perfume disfrutando el ambiente tranquilo de la ciudad romántica Hueantes de llegar a la aldea ecológica Thuy Bieu. Participar en una clase de cocina y disfrutar del almuerzo con las comidas típicas que hemos cocinado.

                            Después de un breve descanso, tomaremos un paseo en bicicleta en la aldea, aprender la vida cotidiana de la gente local.  Luego será un baño de los pies con hierbas medicinales y pasar la noche en HueEcolodge.

                        </div>
                        <div class="title_li_tour">
                            Día 11: Montaña de Mármol - Ciudad antigua HoiAn (D)
                        </div>
                        <div class="content_li_tour">
                            Por la mañana, en coche privado nos dirigiremos hacia HoiAn. En el camino, haremos una parada en las Montañas de Mármol: son cinco picos de piedra caliza con afloramientos de mármol conocidas por su belleza y su red de cuevas. A los pies de la montaña de mármol, existe un lugar religioso importante desde la época de los Cham, y allí se pueden ver muchos artesanos que trabajan a mano los objetos de mármol finos.

                            También pasaremos por el particular paso de Hai Van cruzando la cordillera para llegar a Hue. Un maravilloso y espectacular recorrido, ya que Hai Van es el paso más alto y ofrece maravillosas oportunidades para sacar buenas fotografías mientras pasamos por poblados de pescadores y campos de arroz de Hue, sobre el océano y la playa de Danang, así como de las vistas panorámicas de la playa de Lang Co, el pueblo de la costa, y la laguna muy por debajo. Llegada a HoiAn, comprobar en su hotel.

                            Dedicaremos del resto del día para visitar la ciudad de HoiAn, que representó un puerto de intercambios con extremo oriente hasta finales del s. XIX. Visitaremos la pagoda PhuocKien, el puente cubierto japonés, la casa Tan Ky, con más de 200 años de antigüedad y sus poemas chinos incrustados en madreperla en sus estructuras de madera, un taller de artesanía y recorreremos algunas de las reconocidas 844 casas históricas y templos.
                        </div>
                        <div class="title_li_tour">
                            Día 12: Excursión de Plantar arroz y Pescar con redes (D/A)
                        </div>
                        <div class="content_li_tour">
                            Si les gusta tener una combinación mixta en plantar arroz con los agricultores locales y experimentar de la pesca con los pescadores locales en HoiAn, esta excursión será una cosa maravillosa para probarlo. Este tour les llevará una maravillosa cultura del cultivo de arroz y las tareas de pesca tradicional de la población local en la región central de Vietnam.

                            Recogida en el hotel por un coche, les llevará al campo donde las bicicletas están disponibles para un paseo interesante. Si no sabe cómo montar en bicicleta, irán usando nuestro servicio scooter "XE OM"). Empezaremos el día en bicicleta al pueblo ecológico CamThanh. Aquí seremos recibidos por un anfitrión local y luego nos unimos con ellos a cultivar arroz con su familia.

                            Iniciaremos las actividades agrícolas como arar y rastrillar con el búfalo, que montar en búfalo de agua, aprender a trasplantar el arroz, moler el arroz, etc.

                            A continuación, nos dirigiremos en coche hacia un pueblo de pescadores. Embarcaremos a un barco uniéndose ir a pescar con los pescadores locales para experimentar la pesca con red. Continuaremos la visita al bosque de palmas de coco, disfrutando de un paseo en barco-cesta y  aprender cómo hacen tales como saltamontes, flores, anillos, gorros, juguetes, pulseras hechas de hojas de coco. Tendrán momentos divertidos cuando coger cangrejos en  el bosque y después regresaremos al barco.  El Capitán Cook cocinará el almuerzo para todos sirviendo con cerveza fría o refresco. Regreso a HoiAn.
                        </div>
                        <div class="title_li_tour">
                            Día 13: Vuelo a Ho Chi Minh - Descubrir la vida nocturna Ho Chi Minh (D) Guía en español
                        </div>
                        <div class="content_li_tour">
                            A la hora indicada traslado al aeropuerto sin guía para tomar el vuelo a Ho Chi Minh. Recepción por un chofer e ir al hotel. Tarde libre.

                            18:00 – 22:00
                            A las 18:00, las motos les espera en el hotel para empezar una visita descubrir la vida nocturna de la ciudad Ho Chi Minh. Harán todas las cosas como una gente local. Visitan en moto atrás de su conductor para disfrutar del ambiente bullicioso de Saigon, degustar algunas comidas típicas en los restaurantes locales y también vamos a ir a una cafetería de estilo vietnamita con música en vivo especial, y luego  a un emocionante bar de música rock, donde la nueva generación vietnamita y alarde de su estilo único.
                            Este tour incluye las bebidas y todo lo que puede comer, así que venga con un apetito!
                        </div>
                        <div class="title_li_tour">
                            Día 14: Ho Chi Minh –En barco y bicicleta en el Delta Mekong (D/A)
                        </div>
                        <div class="content_li_tour">
                            Tras el desayuno, nos dirigiremos en coche hasta la zona del Delta Mekong. A la llegada, embarcaremos a un barco para visitar una casa antigua de la zona. Cambiaremos a un bote de remos en los canales pequeños dirigiéndose hacia la isla Tan Phong donde tendremos un breve descanso antes de empezar la ruta en bici para descubrir la vida de los locales. Almorzaremos en un restaurante local. Si el tiempo libre permite podemos tener la oportunidad escuchar la música local “Don ca Tai Tu” hasta el traslado de regreso a Ho Chi Minh.
                        </div>
                        <div class="title_li_tour">
                            Día 15: Final del viaje (D)
                        </div>
                        <div class="content_li_tour">
                            A la hora convenida, os trasladaremos al aeropuerto sin guía para tomar vuelo al destino siguiente.
