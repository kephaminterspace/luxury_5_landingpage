                        </div>
                        <div class="content_li_tour">
                            Desayuno en el hotel. Hoy realizarán una excusión a Hoa Lu. Hoa Lu, antigua capital de Daicoviet (antiguo nombre de Vietnam), durante 41 años desde el año 968 cuando Dinh Bo Linh subió al trono hasta 1009. Este lugar fue escogido como capital debido a su proximidad con China y también a la protección natural que ofrecían el entorno y el bizarro paisaje. En el año 1010 el rey Ly Thai To traslado la capital a Thang Long, actualmente al llegar a Hoa Lu, del antiguo palacio fundacional solo se conservan dos templos construidos como homenaje a los reyes Dinh y Le. Luego, se dirigirán en bote descendiendo por el río hasta el poblado de TrangAn, conocido como “la seca bahía de Halong”. La combinación de la montaña, el río, los campos de arroz y las cuevas con estalactitas colgantes configuran un escenario indescriptible y único.

