                            Jour 12: Vinh Long – Ho Chi Minh Ville (Ptd)
                        </div>
                        <div class="content_li_tour">
                            8h00: Vous recevez le vélo après le petit-déjeuner pour commencer le voyage en découvrant un itinéraire agréable et participant à de nombreuses activités avec les habitants de l'île de Tan Phong. Tout d'abord, visiter la maison de Mme Ba pour apprendre comment faire le papier de riz utilisé pour faire les plats sping roll. Ensuite, aller à la maison de Mme Sau pour voir la méthode pour tricoter le hamac de la banane par la corde de feuilles de banane sèche. La prochaine destination est la maison de Mme Yen - l'endroit où vous apprenez la façon de faire des gâteaux feuille de jacquier – la spécialité de l'île Tan Phong. Après cela, vous vous rendez à Mme Muoi - une très vieille femme - pour voir la méthode pour faire les feuilles qui peuvent être le toit de la maison vietnamienne. Dites adieu à Mme Muoi, vous allez à la maison de Thao pour savoir tricoter la jacinthe d'eau en prenant leur premier cadeau fait à la main avec la direction de Mme Thao. Ensuite, vous visitez une école pour interagir avec les enfants. Enfin, retour au MekongEcolodge pour prendre un repos et check-out.
                            Vers 12h00: vous ferez l'expérience du ferry-boat Tan Phong vers Hiep Duc puis conduit en direction de l’aéroport international Tan Son Nhat pour le vol de départ.
                            Fin des services. Cam On & Chao Tam Biet.
