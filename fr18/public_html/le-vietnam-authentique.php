                            Nous commençons à découvrir la ville de Saigon comme le Palais de Réunification, la Cathédrale Notre Dame, le Vieux-Poste, le Musée de la Guerre qui abrite une collection impressionnantes de photos prises par des journalistes étrangers pendant la guerre contre les Américains, la pagode GiacLam, la ville de la Chine avec les anciennes pagodes et temples, le Marché Binh Tay.
                            Dîner libre. Nuit à l’hôtel.
                        </div>
                        <div class="title_li_tour">
                            Jour 11: HCMV – Vinh Long - Mékong chez l’habitant (Ptd, Déj, D)
                        </div>
                        <div class="content_li_tour">
                            Avant d'aller à la station des bateaux à Cai Be, la voiture s'arrête à Cai Be Cao Dai Oratoires, qui est le symbole religieux de l'unicité locale. Puis vous serez ramassés par des bateaux pour visiter le marché flottant de riz dans la commune An Cu, district de Cai Be - où est la plus grande région d’exportation de riz du sud-ouest au Vietnam, les touristes discuteront pour en savoir plus sur la vie quotidienne des locaux gens. Les destinations suivantes sont l'ancien village de Dong Hoa Hiep et le marché flottant Cai Be. En quittant le marché flottant, les bateaux vous emmènent à l'îlot de Tan Phong et vont à la maison de M.Chin par une chaloupe dans le petit canal. Ici, vous et les propriétaires boivent ensemble du thé, discutent et apprennent sur la vie quotidienne de l'île Tan Phong et comment faire du thé de lotus, confiture de lotus. Ensuite, vous apprécierez du thé de lotus, une confiture de lotus tout en écoutant une mélodie vietnamienne traditionnelle exécutée par M. Chin. Au revoir M. Chin, vous continuez à visiter le verger de Mme. Hang, en dégustant les fruits dans le jardin. La prochaine destination est Mekong Eco restaurant où les clients ont la chance de participer au processus de cuisson et de profiter de leur déjeuner. Après le repas, vous essayerez de tirer les sabots de poisson, l'expérience du pont de singe - la caractéristique unique du Vietnam du Sud et de prendre un repos sur le hamac tissé par la corde de banane. Ce sera certainement très intéressant. Ensuite, vous allez profiter une balade en bateau à rames dans les petits canaux et s'arrêter à MekongEcolodge pour l’installation.
                            Vers 4h: vous participez à l'activité de pêche et de cueillette des légumes dans le jardin du restaurant pour préparer le dîner. Ensuite, vous et l’équipe d'Ecolodge cuisineront ensemble et apprécieront notre repas.
                            Nuit à Mékong Écolodge.
                        </div>
