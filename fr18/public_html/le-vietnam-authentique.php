                            Jour 7: Hue – Village ThanhThuy – visite en groupe (Ptd, Déj)
                        </div>
                        <div class="content_li_tour">
                            A 8 kilomètres de l’ancienne cité impériale Hué, se trouve un petit village qui préserve encore tous les charmes de la campagne. Il s’agit de ThanhThuy, connu aussi pour son pont couvert atypique appelé ThanhToàn… et vieux de plus de 200 ans.
                            8h00: Pick up à votre hôtel vers le centre-ville Hue- ToaKham.
                            8h30: Commencer à vélo avec votre guide vers ThanhThuy Village où a ThanhToan Pont, l'un des célèbres ponts anciens au Vietnam. Sur le chemin, vous verrez les champs de riz et de visiter quelques grands temples comme Ngo Tran et les Temples.
                            9h15: Détendre et profiter de notre boisson de bienvenue. Marche / VTT autour du village, visiter le marché local pour le bien - comprendre la vie quotidienne des gens d'ici, ThanhToan Pont Toit de tuiles et des outils Agricoles musée avec de nombreux types d'outils dans le vieux jour où les agriculteurs ont utilisé sur les champs de riz.
                            9h45: ramer avec le villageois sur le fleuve Nhu Y pour profiter de paysages spectaculaires et d'explorer comment ils attrapent les poissons à l'aide de quelques vieux outils comme piège de pêche, coulée net ... Après avoir observé, vous sera vécue avec eux pour attraper le poisson .
                            11h00: Découverte la procédure de l'éclosion de l'œuf de canard caneton. Après cela, de voir les activités de la ferme.
                            11h30: Rejoignez cours de cuisine avec notre chef. Apprenez les méthodes traditionnelles de préparation et de cuisson des aliments spéciaux vietnamiens. Après la leçon, appréciez ce que vous avez fait cuire.
                            13h30: Revenez ToaKhamà vélo. Puis visite de la pagode ThiênMụ (Dame Céleste), Dîner  libre. Nuit à votre hôtel.
                        </div>
                        <div class="title_li_tour">
                            Jour 8: Hue – Hoian (Ptd)
                        </div>
                        <div class="content_li_tour">
                            Ce matin vous découvrirez l'ancienne capitale impériale : la Citadelle, la Cité Pourpre Interdite, et le Musée Royal. La Cité Pourpre Interdite témoigne de l'opulence et de la grandeur qu'a connu Hué du temps des empereurs. Grâce à la participation de l'UNESCO, ce site a été fidèlement restauré pour le plus grand plaisir de tous. Ce site a été construit sur le modèle de la Cité Interdite de Pékin et regroupe de nombreux temples et palais. Vous entrerez l'immense porte du Midi, puis traverserez la salle du Trône avant de vous retrouver dans l'enceinte de la Cité. Une des parties les plus intéressantes de cette Cité, est le Temple Ancestral, dédié aux Empereurs décédés. Comme il est de coutume au Vietnam, des autels sont construits où chaque personne décédée sera vénérée régulièrement par sa famille.
                            Puis route pour Hoian en empruntant la route charmante du col des Nuages qui offre à son sommet un superbe panorama sur le littoral du Centre, vous arrêtez à la plage Langco, l’une des plus belles péninsules du Vietnam où vous pouvez prendre de superbes photos sur le village des pêcheurs. Arrivée à Hoian. Check-in à l’hôtel.
                            Dîner libre. Nuit à votre hôtel.
