                            Jour 9 : Hoian (Ptd)
                        </div>
                        <div class="content_li_tour">
                            Hoian est une petite ville tranquille, populaire pour son architecture éclectique, ses magasins de souvenirs, ses nombreux tailleurs et couturiers et ses cafés. Le matin vous pouvez faire des activités à votre maison chez l’habitant comme faire du vélo, cours de cuisine, faire des kites … (tout est inclus)
                            En début d’après-midi, vous partirez avec votre guide faire un tour privé de la ville. Visite des rues du vieux quartier, vous verrez : la pagode ChuaOng, la salle de l'Assemblée, à la maison Tam Ky, vieille de 200 ans, les maisons de négociants chinois, et le pont couvert japonais. Le Hall d'Assemblée PhuocKien, construit en 1690, est un site à ne pas manquer. La pagode qu'il abrite est dédiée à ThienHauThanhMau, déesse de la mer et protectrice des marins. L'emblème non officieux de la ville est le pont couvert japonais, vieux de 400 ans, que vous apercevrez sur la route.
                            Hoian est connue comme une ville des lanternes multi couleurs, vous allez apprendre sur l’histoire, la signification et la fabrication des différents types de lanternes. Vous en réaliserez par vous-même comme un souvenir sympa à ramener chez vous, cela est également une bonne manière de tester vos aptitudes aux travaux manuels et votre sens de l’esthétisme.
                            Dîner libre. Nuit à l’hôtel.
                        </div>
                        <div class="title_li_tour">
                            Jour 10 : Hoian – Ho Chi Minh Ville (Ptd)
                        </div>
                        <div class="content_li_tour">
